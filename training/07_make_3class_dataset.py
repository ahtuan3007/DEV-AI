"""
Create a 3-class YOLO dataset from the current 4-class dataset.

Why:
  Binh_Thuong should be the app's idle state, not a YOLO bbox class.
  This script removes Binh_Thuong boxes, keeps those images as negative
  background samples, and remaps action classes to:

    0 Nam_Tay
    1 Chi_Ngon_Tro
    2 Xoe_Tay

Run:
  python training/07_make_3class_dataset.py --clean --zip-output dataset_3class.zip
"""

from __future__ import annotations

import argparse
import shutil
import zipfile
from collections import Counter
from pathlib import Path

BASE_DIR = Path(__file__).resolve().parent.parent
DEFAULT_SOURCE_DIR = BASE_DIR / "dataset"
DEFAULT_OUTPUT_DIR = BASE_DIR / "dataset_3class"
IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}

OLD_TO_NEW_CLASS = {
    1: 0,  # Nam_Tay
    2: 1,  # Chi_Ngon_Tro
    3: 2,  # Xoe_Tay
}

CLASS_NAMES = ["Nam_Tay", "Chi_Ngon_Tro", "Xoe_Tay"]

DATA_YAML = """# Generated by training/07_make_3class_dataset.py
path: /content/dataset
train: images/train
val: images/val

names:
  0: Nam_Tay
  1: Chi_Ngon_Tro
  2: Xoe_Tay
"""


def parse_label_line(line: str, label_path: Path, line_number: int) -> tuple[int, list[str]]:
    parts = line.split()
    if len(parts) != 5:
        raise ValueError(f"{label_path}:{line_number} expected 5 YOLO columns, got {len(parts)}")
    class_id = int(float(parts[0]))
    coords = [float(value) for value in parts[1:]]
    if any(value < 0.0 or value > 1.0 for value in coords):
        raise ValueError(f"{label_path}:{line_number} bbox values must be normalized 0..1")
    return class_id, [f"{value:.6f}" for value in coords]


def convert_label_file(label_src: Path, label_dest: Path) -> tuple[int, Counter[int], int]:
    kept_lines: list[str] = []
    kept_counts: Counter[int] = Counter()
    removed_background = 0

    if label_src.exists():
        for line_number, raw_line in enumerate(label_src.read_text(encoding="utf-8").splitlines(), start=1):
            line = raw_line.strip()
            if not line:
                continue
            old_class_id, coords = parse_label_line(line, label_src, line_number)
            if old_class_id == 0:
                removed_background += 1
                continue
            if old_class_id not in OLD_TO_NEW_CLASS:
                raise ValueError(f"{label_src}:{line_number} unknown class id {old_class_id}")
            new_class_id = OLD_TO_NEW_CLASS[old_class_id]
            kept_counts[new_class_id] += 1
            kept_lines.append(" ".join([str(new_class_id), *coords]))

    label_dest.parent.mkdir(parents=True, exist_ok=True)
    label_dest.write_text("\n".join(kept_lines) + ("\n" if kept_lines else ""), encoding="utf-8")
    return len(kept_lines), kept_counts, removed_background


def convert_split(source_dir: Path, output_dir: Path, split: str) -> tuple[int, int, int, Counter[int]]:
    src_images = source_dir / "images" / split
    src_labels = source_dir / "labels" / split
    dest_images = output_dir / "images" / split
    dest_labels = output_dir / "labels" / split

    if not src_images.exists():
        raise SystemExit(f"Missing source image folder: {src_images}")
    if not src_labels.exists():
        raise SystemExit(f"Missing source label folder: {src_labels}")

    dest_images.mkdir(parents=True, exist_ok=True)
    dest_labels.mkdir(parents=True, exist_ok=True)

    image_count = 0
    positive_image_count = 0
    removed_background_boxes = 0
    class_counts: Counter[int] = Counter()

    for image_src in sorted(path for path in src_images.iterdir() if path.suffix.lower() in IMAGE_EXTENSIONS):
        image_dest = dest_images / image_src.name
        label_src = src_labels / image_src.with_suffix(".txt").name
        label_dest = dest_labels / image_src.with_suffix(".txt").name

        shutil.copy2(image_src, image_dest)
        kept_box_count, kept_counts, removed_count = convert_label_file(label_src, label_dest)

        image_count += 1
        if kept_box_count:
            positive_image_count += 1
        class_counts.update(kept_counts)
        removed_background_boxes += removed_count

    return image_count, positive_image_count, removed_background_boxes, class_counts


def zip_dataset(dataset_dir: Path, zip_path: Path) -> None:
    if zip_path.exists():
        zip_path.unlink()
    with zipfile.ZipFile(zip_path, mode="w", compression=zipfile.ZIP_DEFLATED) as archive:
        for path in sorted(dataset_dir.rglob("*")):
            if path.is_file():
                archive.write(path, arcname=Path("dataset") / path.relative_to(dataset_dir))


def main() -> None:
    parser = argparse.ArgumentParser(description="Build a 3-class YOLO dataset without Binh_Thuong boxes.")
    parser.add_argument("--source-dir", type=Path, default=DEFAULT_SOURCE_DIR)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--clean", action="store_true")
    parser.add_argument("--zip-output", type=Path, help="Optional zip file for Colab upload.")
    args = parser.parse_args()

    source_dir = args.source_dir.resolve()
    output_dir = args.output_dir.resolve()
    if not source_dir.exists():
        raise SystemExit(f"Source dataset not found: {source_dir}")

    if args.clean and output_dir.exists():
        shutil.rmtree(output_dir)

    (output_dir / "images").mkdir(parents=True, exist_ok=True)
    (output_dir / "labels").mkdir(parents=True, exist_ok=True)

    total_classes: Counter[int] = Counter()
    print("3-class dataset conversion:")
    for split in ["train", "val"]:
        images, positive_images, removed_boxes, class_counts = convert_split(source_dir, output_dir, split)
        total_classes.update(class_counts)
        negative_images = images - positive_images
        print(f"  {split}: {images} images | {positive_images} positive | {negative_images} negative/background")
        print(f"    removed Binh_Thuong boxes: {removed_boxes}")
        for class_id, class_name in enumerate(CLASS_NAMES):
            print(f"    {class_id} {class_name}: {class_counts[class_id]} boxes")

    (output_dir / "data.yaml").write_text(DATA_YAML, encoding="utf-8")
    print(f"\nCreated dataset: {output_dir}")
    print("Total action boxes:")
    for class_id, class_name in enumerate(CLASS_NAMES):
        print(f"  {class_id} {class_name}: {total_classes[class_id]}")

    if args.zip_output:
        zip_path = args.zip_output.resolve()
        zip_dataset(output_dir, zip_path)
        print(f"\nCreated zip for Colab: {zip_path}")


if __name__ == "__main__":
    main()
