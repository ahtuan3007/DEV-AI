"""
Đóng gói dataset YOLO: chia train/val + tạo data.yaml

Dùng sau khi export từ Roboflow (YOLOv8) hoặc khi đã có cặp images/labels.

Chạy:
  python training/02_package_dataset.py --source D:/path/to/roboflow_export
  python training/02_package_dataset.py --source data/roboflow_export --val-ratio 0.2
"""

from __future__ import annotations

import argparse
import random
import shutil
from pathlib import Path

BASE_DIR = Path(__file__).resolve().parent.parent
DATASET_DIR = BASE_DIR / "dataset"

CLASS_NAMES = ["Binh_Thuong", "Nam_Tay", "Chi_Ngon_Tro", "Xoe_Tay"]

DATA_YAML = """# Generated by training/02_package_dataset.py
path: /content/dataset
train: images/train
val: images/val

names:
  0: Binh_Thuong
  1: Nam_Tay
  2: Chi_Ngon_Tro
  3: Xoe_Tay
"""


def find_image_label_pairs(root: Path) -> list[tuple[Path, Path]]:
    """Tìm cặp (image, label) trong cấu trúc Roboflow hoặc flat."""
    pairs: list[tuple[Path, Path]] = []
    image_exts = {".jpg", ".jpeg", ".png", ".bmp"}

    for img_path in root.rglob("*"):
        if img_path.suffix.lower() not in image_exts:
            continue
        label_path = img_path.with_suffix(".txt")
        if not label_path.exists():
            # Roboflow: labels ở thư mục song song images -> labels
            rel = img_path.relative_to(root)
            parts = list(rel.parts)
            if "images" in parts:
                idx = parts.index("images")
                parts[idx] = "labels"
                alt = root / Path(*parts).with_suffix(".txt")
                if alt.exists():
                    label_path = alt
            if not label_path.exists():
                continue
        pairs.append((img_path, label_path))

    return pairs


def pairs_already_split(pairs: list[tuple[Path, Path]], root: Path) -> bool:
    """Roboflow thường đã có train/val."""
    for img, _ in pairs[:5]:
        rel = str(img.relative_to(root)).lower()
        if "train" in rel or "valid" in rel or "val" in rel:
            return True
    return False


def copy_split(
    pairs: list[tuple[Path, Path]],
    split_name: str,
    dest_images: Path,
    dest_labels: Path,
) -> int:
    dest_images.mkdir(parents=True, exist_ok=True)
    dest_labels.mkdir(parents=True, exist_ok=True)
    count = 0
    for img_src, lbl_src in pairs:
        name = f"{img_src.stem}_{count:06d}{img_src.suffix.lower()}"
        shutil.copy2(img_src, dest_images / name)
        shutil.copy2(lbl_src, dest_labels / Path(name).with_suffix(".txt"))
        count += 1
    return count


def group_by_video_stem(pairs: list[tuple[Path, Path]]) -> dict[str, list[tuple[Path, Path]]]:
    groups: dict[str, list[tuple[Path, Path]]] = {}
    for img, lbl in pairs:
        # frame_00001 từ binh_thuong_00012.jpg -> nhóm theo prefix trước số cuối
        stem = img.stem.rsplit("_", 1)[0] if "_" in img.stem else img.stem
        groups.setdefault(stem, []).append((img, lbl))
    return groups


def split_train_val(
    pairs: list[tuple[Path, Path]], val_ratio: float, seed: int
) -> tuple[list[tuple[Path, Path]], list[tuple[Path, Path]]]:
    """Chia theo nhóm video/session để tránh leak."""
    groups = group_by_video_stem(pairs)
    keys = sorted(groups.keys())
    random.Random(seed).shuffle(keys)

    n_val = max(1, int(len(keys) * val_ratio))
    val_keys = set(keys[:n_val])

    train_pairs: list[tuple[Path, Path]] = []
    val_pairs: list[tuple[Path, Path]] = []
    for key, group_pairs in groups.items():
        if key in val_keys:
            val_pairs.extend(group_pairs)
        else:
            train_pairs.extend(group_pairs)
    return train_pairs, val_pairs


def import_roboflow_pre_split(root: Path) -> None:
    """Copy dataset đã có train/valid từ Roboflow."""
    for split_src, split_dst in [("train", "train"), ("valid", "val"), ("val", "val")]:
        img_src = None
        for candidate in [
            root / split_src / "images",
            root / "images" / split_src,
        ]:
            if candidate.exists():
                img_src = candidate
                break
        if img_src is None:
            continue

        lbl_src = img_src.parent / "labels"
        if not lbl_src.exists():
            lbl_src = img_src.parent.parent / "labels" / split_src
        if not lbl_src.exists() and split_src == "valid":
            lbl_src = root / "valid" / "labels"

        dest_img = DATASET_DIR / "images" / split_dst
        dest_lbl = DATASET_DIR / "labels" / split_dst
        if dest_img.exists():
            shutil.rmtree(dest_img)
        if dest_lbl.exists():
            shutil.rmtree(dest_lbl)

        shutil.copytree(img_src, dest_img)
        if lbl_src.exists():
            shutil.copytree(lbl_src, dest_lbl)


def main() -> None:
    parser = argparse.ArgumentParser(description="Đóng gói dataset YOLO")
    parser.add_argument("--source", type=str, required=True, help="Thư mục export Roboflow (giải nén)")
    parser.add_argument("--val-ratio", type=float, default=0.2, help="Tỷ lệ val nếu tự chia")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--clean", action="store_true", help="Xóa dataset/ cũ trước khi tạo mới")
    args = parser.parse_args()

    source = Path(args.source).resolve()
    if not source.exists():
        raise SystemExit(f"Not found: {source}")

    if args.clean and DATASET_DIR.exists():
        shutil.rmtree(DATASET_DIR)

    pairs = find_image_label_pairs(source)
    if not pairs:
        raise SystemExit(
            "No image + .txt label pairs found. "
            "Export YOLOv8 from Roboflow or check --source path."
        )

    print(f"Found {len(pairs)} image-label pairs.")

    if pairs_already_split(pairs, source):
        print("Detected pre-split train/val (Roboflow) — copying.")
        import_roboflow_pre_split(source)
    else:
        print(f"Splitting train/val ({1 - args.val_ratio:.0%}/{args.val_ratio:.0%}) by video group.")
        train_pairs, val_pairs = split_train_val(pairs, args.val_ratio, args.seed)
        copy_split(train_pairs, "train", DATASET_DIR / "images" / "train", DATASET_DIR / "labels" / "train")
        copy_split(val_pairs, "val", DATASET_DIR / "images" / "val", DATASET_DIR / "labels" / "val")
        print(f"  train: {len(train_pairs)} | val: {len(val_pairs)}")

    yaml_path = DATASET_DIR / "data.yaml"
    DATASET_DIR.mkdir(parents=True, exist_ok=True)
    yaml_path.write_text(DATA_YAML, encoding="utf-8")

    print(f"\nCreated: {DATASET_DIR}")
    print(f"  - {yaml_path}")
    print("\nNext:")
    print("  1. Zip dataset/ -> dataset.zip")
    print("  2. Upload to Google Drive/MyDrive/SmartHospital/")
    print("  3. Open training/colab_train.ipynb on Colab")


if __name__ == "__main__":
    main()
